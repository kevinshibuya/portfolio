import { readFileSync, writeFileSync } from 'node:fs'
import { resolve, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'
import opentype from 'opentype.js'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = resolve(__dirname, '..')

const FONT_PATH = resolve(ROOT, 'public/fonts/PlusJakartaSans-VariableFont_wght.ttf')
const OUT_PATH = resolve(ROOT, 'src/data/glyphPaths.ts')

const KEVIN = 'kevin'
const SHIBUYA = 'shibuya.'

const FONT_SIZE = 1000

const fontBuffer = readFileSync(FONT_PATH)
const font = opentype.parse(
  fontBuffer.buffer.slice(
    fontBuffer.byteOffset,
    fontBuffer.byteOffset + fontBuffer.byteLength
  )
)

function extract(word) {
  const glyphs = []
  let cursorX = 0
  for (const ch of word) {
    const glyph = font.charToGlyph(ch)
    const path = glyph.getPath(cursorX, 0, FONT_SIZE)
    const advance = (glyph.advanceWidth / font.unitsPerEm) * FONT_SIZE
    glyphs.push({
      char: ch,
      d: path.toPathData(2),
      advance,
      x: cursorX,
    })
    cursorX += advance
  }
  return { glyphs, totalAdvance: cursorX }
}

const kevin = extract(KEVIN)
const shibuya = extract(SHIBUYA)

const ts = `// AUTO-GENERATED by scripts/extract-glyph-paths.mjs. Do not hand-edit.
// To regenerate: \`node scripts/extract-glyph-paths.mjs\`

export interface GlyphPath {
  char: string
  d: string
  advance: number
  x: number
}

export interface NameWord {
  glyphs: GlyphPath[]
  totalAdvance: number
}

export const NAME_FONT_EM = ${FONT_SIZE}

export const NAME_KEVIN: NameWord = ${JSON.stringify(kevin, null, 2)}

export const NAME_SHIBUYA: NameWord = ${JSON.stringify(shibuya, null, 2)}
`

writeFileSync(OUT_PATH, ts)
console.log(`Wrote ${OUT_PATH}`)
console.log(`  kevin: ${kevin.glyphs.length} glyphs, totalAdvance=${kevin.totalAdvance.toFixed(1)}`)
console.log(`  shibuya.: ${shibuya.glyphs.length} glyphs, totalAdvance=${shibuya.totalAdvance.toFixed(1)}`)
