import { readFileSync, writeFileSync } from 'node:fs'
import { resolve, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'
import opentype from 'opentype.js'

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = resolve(__dirname, '..')

// Use the static Bold (700) instance produced by fontTools — opentype.js
// can't apply variation values reliably, so we extract from a pre-instanced
// static font. Regenerate via:
//   python3 -c "from fontTools.ttLib import TTFont; \
//     from fontTools.varLib.instancer import instantiateVariableFont; \
//     f=TTFont('public/fonts/PlusJakartaSans-VariableFont_wght.ttf'); \
//     instantiateVariableFont(f, {'wght': 700}).save('public/fonts/PlusJakartaSans-Bold.ttf')"
const FONT_PATH = resolve(ROOT, 'public/fonts/PlusJakartaSans-Bold.ttf')
const OUT_PATH = resolve(ROOT, 'src/data/glyphPaths.ts')

const KEVIN = 'kevin'
const SHIBUYA = 'shibuya.'
const FONT_SIZE = 1000

const fontBuffer = readFileSync(FONT_PATH)
const font = opentype.parse(
  fontBuffer.buffer.slice(
    fontBuffer.byteOffset,
    fontBuffer.byteOffset + fontBuffer.byteLength
  )
)

/** opentype.js's toPathData omits the implicit `Z` (close-path) at the
 *  end of each subpath. SVG fill auto-closes, but STROKE doesn't — so
 *  without Z, the closing edge of every glyph's outline is left out
 *  and the trace looks like it's missing strokes (e.g. the right side
 *  of "v"). We splice Z in by hand: prepend Z before every non-initial
 *  M, and append a final Z. */
function closeSubpaths(d) {
  const closed = d.replace(/(?<!^)M/g, 'ZM')
  return closed.endsWith('Z') ? closed : `${closed}Z`
}

function extract(word) {
  const glyphs = []
  let cursorX = 0
  for (const ch of word) {
    const glyph = font.charToGlyph(ch)
    const path = glyph.getPath(cursorX, 0, FONT_SIZE)
    const advance = (glyph.advanceWidth / font.unitsPerEm) * FONT_SIZE
    glyphs.push({
      char: ch,
      d: closeSubpaths(path.toPathData(2)),
      advance,
      x: cursorX,
    })
    cursorX += advance
  }
  return { glyphs, totalAdvance: cursorX }
}

const kevin = extract(KEVIN)
const shibuya = extract(SHIBUYA)

const ts = `// AUTO-GENERATED by scripts/extract-glyph-paths.mjs. Do not hand-edit.
// To regenerate: \`node scripts/extract-glyph-paths.mjs\`
// Source font: PlusJakartaSans-Bold.ttf (weight 700, instanced via fontTools).

export interface GlyphPath {
  char: string
  d: string
  advance: number
  x: number
}

export interface NameWord {
  glyphs: GlyphPath[]
  totalAdvance: number
}

export const NAME_FONT_EM = ${FONT_SIZE}

/** viewBox top (negative Y, since glyphs render above the baseline). */
export const NAME_ASCENT = ${Math.round(FONT_SIZE * 0.82)}
/** viewBox bottom (positive Y, descender region for letters like y, p, g). */
export const NAME_DESCENT = ${Math.round(FONT_SIZE * 0.25)}

export const NAME_KEVIN: NameWord = ${JSON.stringify(kevin, null, 2)}

export const NAME_SHIBUYA: NameWord = ${JSON.stringify(shibuya, null, 2)}
`

writeFileSync(OUT_PATH, ts)
console.log(`Wrote ${OUT_PATH}`)
console.log(`  kevin: ${kevin.glyphs.length} glyphs, totalAdvance=${kevin.totalAdvance.toFixed(1)}`)
console.log(`  shibuya.: ${shibuya.glyphs.length} glyphs, totalAdvance=${shibuya.totalAdvance.toFixed(1)}`)
